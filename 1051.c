#include<stdio.h>

int main()
{
    float x,s,p,r,q;
    scanf("%f",&x);
    if(x>=0.00 && x<=2000.00)
    {
        printf("Isento\n");
    }
    else if(x>=2000.01 && x<=3000.00)
    {
        s=((x-2000.00)*8)/100.00;
        printf("R$ %.2f\n",s);
    }
    else if(x>=3000.01 && x<=4500.00)
    {
        s=((x-3000.00)*18)/100.00;
        p=((3000.00-2000.00)*8)/100.00;
        printf("R$ %.2f\n",p+s);
    }
    else
    {
        s=(x-4500.00);
        p=(4500.00-3000.00);
        q=(3000.00-2000.00);
        r=((s*28)/100.00)+((p*18)/100.00)+((q*8)/100.00);
        printf("R$ %.2f\n",r);
    }
    return 0;
}

